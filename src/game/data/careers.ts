/**
 * Devuelve las carreras disponibles para un jugador según sus stats.
 * Solo incluye carreras cuyos requisitos el jugador cumple.
 */
export function getAvailableCareers(stats: PlayerStats): Career[] {
  return CAREERS.filter((career) => {
    // Illegal careers always visible (you can always choose the dark side)
    if (career.category === 'ilegal') return true;

    return (
      stats.education >= career.requiredEducation &&
      stats.charisma >= career.requiredCharisma &&
      stats.intelligence >= career.requiredIntelligence
    );
  });
}
